// Баг 3 (Important, повторное ревью Task 3): CO_IN всегда берёт ПОСЛЕДНЕЕ «в X»
// в цепочке, считая его работодателем. Но для зеркального порядка «роль в
// <РеальнаяКомпания> в <Подразделение>» последнее «в X» — это подразделение,
// а не работодатель. Правило должно быть общим: перебирать кандидатов цепочки
// с конца и брать первого, кто переживёт cleanCo (стоп-лист оргединиц бракует
// подразделение — тогда откат к настоящей компании перед ним).
test("реальный пост: «роль в Яндекс в Департамент Такси» — компания «Яндекс», а не подразделение", () => {
  const r = parseTgJob(
    "Разработчик в Яндекс в Департамент Такси. Москва.",
    [],
  );
  assert.equal(r.company, "Яндекс");
});

