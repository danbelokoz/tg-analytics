// Находка ревью: прежние тесты давали 0, 1 или 3 совпадения — ни одного
// случая ровно с двумя, поэтому порог «2 из 3» ничем не был закреплён (замена
// на «3 из 3» осталась бы незамеченной). Ниже — регрессионные тесты именно на
// границу: ровно два признака из трёх, в двух разных сочетаниях.

// (а) заголовок «Резюме:» + контакт (email), БЕЗ анкетных полей — реальный
// пост https://t.me/jobster_resume/492 («Резюме: Junior CRM-маркетинг»).
// В тексте есть «Формат: Удаленка», но RESUME_FIELDS требует именно
// «Формат/Локация» (со слэшем) — тут слэша нет, поле не совпадает, и в посте
// нет ни «Возраст:», ни «Опыт работы:», ни «О себе:» — итог ровно 2 признака
// (заголовок + email), анкетных полей нет вовсе.
test("ровно 2 признака (заголовок + контакты, без анкетных полей) — резюме опознаётся (реальный пост jobster_resume/492)", () => {
  const t = "Резюме: Junior CRM-маркетинг\n\n" +
    "👨‍💻 Александр\nНик tg: @riskyprice\nEmail: Dovzhik.AD@ya.ru\n\nФормат: Удаленка\n\n" +
    "Ищу команду, в которой смогу забрать на себя операционную рутину по рассылкам, " +
    "разгрузить старших специалистов и помогать базе окупаться";
  assert.equal(isResumePost(t), true);
});

// (б) контакты + анкетные поля, БЕЗ заголовка «Резюме:»/«CV:» — синтетический
