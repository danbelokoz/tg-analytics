// Разбор TG-поста: должность и компания. Чистые функции, без DOM — подключаются
// и в браузер (<script src>), и в Node (тесты, скрипт охвата).

// Эвристический разбор TG-поста: должность (первая содержательная строка) и
// компания по паттернам «<роль> в <Компания>», «роль / Компания», «<Company> is a…».
// Неидеально (~65%): дайджесты и анонимные посты остаются без company — тогда
// карточка показывает как раньше (канал + текст).
// Местоимения и глагольные зачины («Мы», «Работаем …», «Наша команда») тоже
// сюда — это не название компании, а начало рассказа о себе после метки
// «Работодатель:». \b не годится для кириллицы (см. комментарий у CO_HIRES
// ниже), поэтому используем lookahead «дальше не буква/цифра».
// Города и страны — после «в X» это почти всегда география, а не работодатель
// (стемы покрывают падежи: «Узбекистане», «Беларуси», «Грузии» и т. п.).
// «департамент», «дивизион», «юнит», «подразделени», «дирекци», «служб»,
// «блок», «групп» (плюс уже имевшиеся «отдел», «направлени», «команд»,
// «проект», «компани») — стемы оргединиц: в цепочке «в X в Y» подразделение
// (а не X) может оказаться последним «в»-кандидатом, его нужно браковать так
// же надёжно, как города — см. перебор кандидатов с конца в CO_IN ниже.
const CO_STOP = /^(москв|петербург|спб|санкт|росси|remote|удал|дистанц|офис|гибрид|команд|стартап|компани|проект|ваканс|it\b|hr\b|финтех|екатеринб|новосиб|казан|лондон|сша|европ|азия|dubai|дубай|поиске|связи|течение|отдел|сфер|област|направлени|штат|найм|лавк|узбекистан|казахстан|беларус|армени|грузи|серби|кипр|оаэ|турци|департамент|дивизион|юнит|подразделени|дирекци|служб|блок|групп|мы(?![\p{L}\p{N}])|наш\p{L}*|работа(?:ем|ю|ет)(?![\p{L}\p{N}])|ищем(?![\p{L}\p{N}]))/iu;
// Аббревиатуры и слова-должности, ошибочно попадающие в «компанию»
// (напр. «Chief Technology Officer / CTO» → «CTO» — это должность, не компания).
const ROLE_WORD = /^(c[a-z]?o|ciso|vp|pm|po|pmm|ba|sa|qa|tl|hrd|hrbp|smm|pr|ux|ui|team\s?lead|tech\s?lead|(?:back|front|full[\s-]?stack)[\s-]?end|developer|engineer|manager|designer|analyst|architect|specialist|lead|intern|разработчик\w*|инженер\w*|менеджер\w*|дизайнер\w*|аналитик\w*|директор\w*|руководител\w*|специалист\w*|стажёр\w*|тимлид\w*|хирург\w*)$/i;
// Служебный префикс-обозначение перед настоящим именем: «Компания X», «Фирма X»,
// «Агентство X». Кандидат целиком, состоящий ТОЛЬКО из такого слова («Компания»,
// «компанию» без ничего после), по-прежнему бракуется ниже через CO_STOP (стем
// «компани» матчит его целиком, если после слова больше ничего нет). А здесь —
// только снятие префикса, когда после него идёт настоящее имя.
const CO_DESIGNATOR = /^(?:компани[яию]|фирм[ауе]|агентств[оа]|организаци[яию])\s+(?=\S)/iu;
function cleanCo(s){
  s = (s||"").replace(/^[^\p{L}\p{N}]+/u,"").trim();
  s = s.replace(CO_DESIGNATOR, "");
  // Перечисление через слэш («Reels / TikTok / посты / карусели / Stories») —
  // это форматы/инструменты, не название компании. Один слэш — нормально
  // (паттерн «роль / Компания» в titleFrom уже вырезает его до вызова cleanCo,
  // так что сюда попадает готовое имя без слэша); два и больше — точно список.
  if ((s.match(/\//gu) || []).length >= 2) return null;
  // «Cloud .ru» — пробел перед доменным суффиксом (частый приём в TG-постах,
  // чтобы мессенджер не подхватывал ссылку в превью). Склеиваем обратно в одно
  // слово ДО нарезки по пунктуации, иначе получим обрезок «Cloud» без «.ru».
  s = s.replace(/\s+\.(?=[a-zа-яё]{2,4}(?:[^\p{L}\p{N}]|$))/giu, ".");
  // Отсечь хвост/ссылку/пунктуацию. Точка режет только в конце предложения
  // (после неё пробел или конец строки) — иначе «Cloud.ru» резался бы посреди
  // домена на первой же точке.
  s = s.split(/\s{2,}|https?:|[,:;•|]|\.(?=\s|$)|\s+[—–-]\s+/u)[0].trim();
  s = s.replace(/\s+(?:ищет|ищут|нанимает|hiring|команда).*$/iu,"").trim();
  // ROLE_WORD проверяем по каждому слову кандидата, а не только по кандидату
  // целиком — иначе «Пластический хирург (Москва» проходит целиком, хотя
  // «хирург» внутри явно должность, а не часть имени компании.
  if(!s || s.length<2 || CO_STOP.test(s) || s.split(/\s+/u).some(w=>ROLE_WORD.test(w)) || !/\p{L}/u.test(s)) return null;
  return s.slice(0,40);
}
// Источники компании по убыванию надёжности. Каждый возвращает сырого кандидата,
// его чистит cleanCo (стоп-листы городов и слов-должностей).
const CO_LABEL = /^(?:компания|работодатель|о компании)\s*[:—-]\s*(.*)$/iu;
// \b тут не годится: JS-регэкспы считают word-символами только [A-Za-z0-9_],
// поэтому граница после кириллического «ищет» перед пробелом не находится —
// используем lookahead «дальше не буква/цифра» вместо \b.
const CO_HIRES = /^[«"“]?([\p{Lu}\p{N}][^«»"”\n]{1,40}?)[»"”]?\s+(?:ищет|ищут|нанимает|в поиске)(?![\p{L}\p{N}])/u;
const CO_IS_A  = /^([\p{Lu}][\w .&'’-]{1,40}?)\s+is\s+(?:a|an|the|one)\b/u;
// «роль в <подбренд> в <Компания>» — частая конструкция («NLP-разработчик в
// Алису в Яндекс», «Руководитель команды разработки в ОК в VK»): работодатель —
// последнее «в X» в цепочке. Но у того же оборота бывает и зеркальный порядок
// «роль в <РеальнаяКомпания> в <Подразделение>» («Разработчик в Яндекс в
// Департамент Такси»), где последнее «в X» — это подразделение, а не
// работодатель. Направление тут не разглядеть без внешнего знания, поэтому
// правило общее: захватываем ВСЮ цепочку «X в Y в Z…» одной группой (разбор
// кусков — как раньше: не-жадные промежутки корректно находят каждое
// «в»-разделение), а затем в companyFrom перебираем куски цепочки С КОНЦА и
// берём первый, что переживёт cleanCo — стоп-лист (CO_STOP) бракует подразделения
// («Департамент Такси»), и разбор откатывается к настоящему работодателю перед ним.
const CO_IN    = /(?:^|\s)в\s+((?:[\p{Lu}\p{N}][^,\n:—(]{1,40}?\s+в\s+)*[\p{Lu}\p{N}][^,\n:—(]{1,40})/u;

// Обычная чистка cleanCo сперва: она уже умеет вытащить осмысленное имя из
// простых фраз («Юлия Прудько - писательница, автор книг «Никак»…» → «Юлия
// Прудько», без обращения к кавычкам). Кавычки — это фолбэк только на случай,
// когда фраза начинается с рассказа о себе («Мы — «Бренд», …», «Работаем с
// двумя проектами: «Бренд1» и «Бренд2»…») и CO_STOP браковит местоимение/глагол
// как кандидата: тогда берём то, что в первых кавычках фразы.
function cleanCoLabel(raw) {
  const s = raw || "";
  const plain = cleanCo(s);
  if (plain) return plain;
  const q = s.match(/[«"“]([^»"”]{2,40})[»"”]/u);
  return q ? cleanCo(q[1]) : null;
}

function companyFrom(lines) {
  // 1. Явная метка. Значение может стоять на той же строке или на следующей
  //    («Работодатель:\nСтудия Звёзд, …»). Если кандидат отбракован (мусор,
  //    местоимение без кавычек) — НЕ останавливаемся: цикл идёт дальше сам
  //    (вдруг ниже есть вторая метка), а если ничего не нашлось — управление
  //    провалится в шаг 2 (CO_HIRES/CO_IS_A/CO_IN) и затем в словарь брендов.
  for (let i = 0; i < lines.length; i++) {
    const m = lines[i].match(CO_LABEL);
    if (!m) continue;
    const c = cleanCoLabel(m[1]) || cleanCoLabel(lines[i + 1] || "");
    if (c) return c;
  }
  // 2. «X ищет …» / «X is a …» / «… в X» — в первых строках поста. Идём
  //    построчно сверху вниз (а не паттерн за паттерном по всему тексту):
  //    иначе случайное совпадение CO_HIRES в дальней строке («Ребята активно
  //    ищут…») перебивает надёжный CO_IN из заголовка («…в Exness»).
  for (const ln of lines) {
    for (const rx of [CO_HIRES, CO_IS_A]) {
      const m = ln.match(rx);
      if (!m) continue;
      const c = cleanCo(m[1]);
      if (c) return c;
    }
    // CO_IN захватывает всю цепочку «X в Y в Z…» одной строкой — куски разделены
    // литеральным « в ». Перебираем куски С КОНЦА к началу и берём первый, кто
    // переживёт cleanCo: для «Алису в Яндекс» последний кусок («Яндекс») пройдёт
    // сразу, а для «Яндекс в Департамент Такси» последний кусок отбракуется
    // стоп-листом оргединиц, и разбор откатится к предыдущему куску («Яндекс»).
    const m = ln.match(CO_IN);
    if (!m) continue;
    const chunks = m[1].split(/\s+в\s+/u);
    for (let i = chunks.length - 1; i >= 0; i--) {
      const c = cleanCo(chunks[i]);
      if (c) return c;
    }
  }
  return null;
}

// Известные бренды — последний источник, когда паттерны не сработали. Ищем по
// границам слова, но только в тех же первых 8 непустых строках, что и
// остальные паттерны (см. lines в parseTgJob) — иначе в длинном посте-подборке
// без тега «Дайджест» можно случайно зацепить бренд из середины списка вакансий,
// а не из текущей. Пополнять список по результатам tests/coverage.mjs.
const BRANDS = [
  "Яндекс","Yandex","Ozon","Wildberries","Сбер","Sber","Тинькофф","Т-Банк","Авито","Avito",
  "VK","МТС","Мегафон","Билайн","Альфа-Банк","Газпромбанк","Х5","Лента","Магнит","Самокат",
  "Райффайзен","Точка","Skyeng","Ламода","Lamoda","Циан","Домклик","Купер","Exness","Kaspi",
  "InDrive","Nebius","ClickHouse","JetBrains","Miro","Semrush","Revolut","Plata","Aviasales",
  "Selectel","Cloud.ru","Positive Technologies","Kaspersky","Касперск","2ГИС","2GIS",
];
const BRAND_RX = BRANDS.map(b => [b, new RegExp(
  `(?<![\\p{L}\\p{N}])${b.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}(?![\\p{L}\\p{N}])`, "iu")]);

function brandFrom(lines) {
  // Построчно, а не по склеенному тексту: строка-перечисление (соцсети/
  // инструменты) или строка, которая целиком сводится к «подпись + ссылка»
  // (без ссылки в ней не остаётся ничего, кроме самого бренда), не про
  // работодателя — бренд из неё выглядит как настоящая компания, а это
  // опаснее, чем отсутствие company вовсе.
  // Реальные примеры: «VK https://vk.ru/tova.lyrics» (подпись к ссылке на
  // соцсеть художницы, не работодателя) и «выкладывать контент в наши
  // соцсети: VK, Instagram, YouTube, TikTok» (перечисление).
  // Важно не путать с «PR-менеджер в команду Aviasales: https://…» — здесь
  // ссылка это просто трек-ссылка отклика в конце обычного предложения, а
  // «Aviasales» — настоящий работодатель; после вычитания ссылки в строке
  // остаётся содержательный текст, а не голое название бренда.
  // Важно не путать и с обычным предложением из нескольких запятых («…команду
  // бизнес-юнитов Яндекс Go и Яндекс Пэй, взаимодействовать с…») — запятых в
  // строке может быть много без того, чтобы бренд стоял в списке; перечислением
  // считаем только случай, когда сам бренд вплотную (через запятую/слэш) стоит
  // рядом с соседними позициями списка.
  for (const line of lines) {
    const noUrl = line.replace(/https?:\/\/\S+|www\.\S+/giu, "").trim();
    const hadUrl = noUrl !== line.trim();
    // Запятые/слэши считаем по строке без ссылки — иначе «https://…/…» сама по
    // себе даёт 2+ слэшей (реальный пост morejobs/15925: «…Aviasales: https://adndx.ru/FtENV»).
    const delimCount = (noUrl.match(/[,/]/gu) || []).length;
    for (const [name, rx] of BRAND_RX) {
      const m = rx.exec(line);
      if (!m) continue;
      if (hadUrl) {
        const residual = noUrl.replace(rx, "").replace(/[^\p{L}\p{N}]+/gu, "");
        if (!residual) continue;                                   // голая «Бренд + ссылка», не предложение
      }
      const before = line.slice(0, m.index).replace(/\s+$/u, "");
      const after = line.slice(m.index + m[0].length).replace(/^\s+/u, "");
      const flanked = /[,/]$/u.test(before) || /^[,/]/u.test(after); // бренд вплотную к запятой/слэшу
      if (flanked && delimCount >= 2) continue;                     // список из ≥3 позиций
      return name;
    }
  }
  return null;
}

// Слова, по которым строка опознаётся как должность. Без единого совпадения
// строку заголовком не считаем — иначе в заголовок лезет «Всем привет!».
const ROLE_HINT = /(разработчик|инженер|менеджер|дизайнер|аналитик|директор|руководител|специалист|тимлид|маркетолог|редактор|копирайтер|продюсер|таргетолог|рекрутер|бухгалтер|юрист|тестировщик|стажёр|стажер|ассистент|developer|engineer|manager|designer|analyst|architect|specialist|lead|head\s+of|director|officer|marketer|writer|editor|producer|recruiter|intern|owner|smm|seo|qa|devops|pm\b|po\b|cto|ceo|cmo|cpo)/iu;
const T_LABEL = /^(?:вакансия|должность|позиция|position|role)\s*[:—-]\s*(.*)$/iu;
// \b тут не годится: JS-регэкспы считают word-символами только [A-Za-z0-9_],
// граница перед кириллическим «ищет» после пробела не находится (см. похожий
// комментарий у CO_HIRES) — используем lookbehind «перед не буква/цифра».
// Необязательный «служебный» жадный фрагмент перед должностью («ищем
// срочно менеджера») сделан ленивым (`??`), а не жадным: жадный квантификатор
// в связке с одинаковым классом символов «съедает» первое слово самой
// должности («менеджера» из «менеджера проектов»), оставляя в захвате только
// хвост («проектов») — а тот часто уже не содержит ролевого слова.
const T_HIRES = /(?<![\p{L}\p{N}])(?:ищет|ищем|ищут|требуется|требуются|нужен|нужна|в поиске)\s+(?:[а-яё]+\s+)??([^,.\n(]{3,60})/iu;

// Разбор заголовка (должности) из TG-поста: явная метка «Вакансия:», иначе
// первая строка (если в ней есть ролевое слово), иначе оборот «X ищет <роль>».
function titleFrom(lines, company) {
  // 1. Явная метка.
  for (const ln of lines) {
    const m = ln.match(T_LABEL);
    if (m && ROLE_HINT.test(m[1])) return cleanTitle(m[1]);
  }
  // 2. Первая строка, если в ней есть ролевое слово. Строку вида «Компания
  // ищет менеджера…» сюда не берём — это оборот «X ищет <должность>»,
  // им займётся шаг 3 (он точнее вырезает саму должность из середины фразы).
  const head = cleanTitle(stripCompany(lines[0], company));
  if (head && ROLE_HINT.test(head) && !T_HIRES.test(lines[0])) return head;
  // 3. «… ищет <должность>» в любой из первых строк.
  for (const ln of lines) {
    const m = ln.match(T_HIRES);
    if (m && ROLE_HINT.test(m[1])) return cleanTitle(m[1]);
  }
  return null;
}

// Заголовок без хвостов: ссылка, зарплата, город в скобках, хвостовые хэштеги
// и пунктуация. Оставляем скобки вида «(Data Stack)» — это часть должности.
function cleanTitle(s) {
  return String(s || "")
    .replace(/https?:\S+/gu, "")
    .replace(/\(\s*г\.[^)]*\)/giu, "")
    .replace(/\s+#\S+/gu, "")
    .replace(/^[^\p{L}\p{N}]+/u, "")
    .replace(/[\s:—–,-]+$/u, "")
    .trim()
    .slice(0, 90);
}

// Убрать «в <Компания>» из строки, чтобы компания не дублировалась в заголовке.
// Между «в» и названием бывает описательное слово («в маркетинговое агентство
// About You», «в студию детской киношколы Студия Звёзд») — не-жадный «.*?»
// пропускает его и всё равно находит компанию дальше в хвосте строки.
// Lookbehind перед «в» обязателен: без него «в» само по себе однобуквенное
// и совпадает с последней буквой любого слова на «-в»/«-ов» («проектов»),
// откусывая её от заголовка («Руководитель проектов» → «Руководитель проекто»).
function stripCompany(line, company) {
  if (!company) return line;
  const esc = company.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  return line.replace(new RegExp(`\\s*(?<![\\p{L}\\p{N}])(?:в|at|@)\\s+.*?[«"“]?${esc}[»"”]?.*$`, "iu"), "")
             .replace(new RegExp(`\\s*[/|]\\s*${esc}\\s*$`, "iu"), "");
}

function parseTgJob(text, tags) {
  if (!text) return {};
  if ((tags || []).includes("Дайджест")) return {};   // список многих вакансий — пары не выделить
  const lines = text.split("\n").map(s => s.trim()).filter(Boolean).slice(0, 8);
  if (!lines.length) return {};
  const company = companyFrom(lines) || brandFrom(lines);
  const title = titleFrom(lines, company);
  return { title: title || null, company };
}

